#include <stdint.h>

typedef char u8;
typedef int32_t i32;

i32 addi32(i32 a, i32 b) {
    return a + b;
}

i32 subi32(i32 a, i32 b) {
    return a - b;
}

i32 muli32(i32 a, i32 b) {
    return a * b;
}

i32 divi32(i32 a, i32 b) {
    return a / b;
}

